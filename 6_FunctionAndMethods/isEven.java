public class isEven {
    public static boolean isEven(int a) {
        boolean isEven = true;
        if (a % 2 == 1) {
            isEven = false;
            return isEven;
        } else {
            return isEven;
        }
    }

    public static void main(String args[]) {
        System.out.println(isEven(20));
        System.out.println(isEven(23));
    }

}
